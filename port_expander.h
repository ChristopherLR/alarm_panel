#pragma once

/* Addresses for PORT EXPANDER
 * Assumes IOCON.BANK = 0 */
#define IODIRA   0x00
#define IODIRB   0x01
#define IPOLA    0x02
#define IPOLB    0x03
#define GPINTENA 0x04
#define GPINTENB 0x05
#define DEFVALA  0x06
#define DEFVALB  0x07
#define INTCONA  0x08
#define INTCONB  0x09
#define IOCON    0x0A
#define GPPUA    0x0C
#define GPPUB    0x0D
#define INTFA    0x0E
#define INTFB    0x0F
#define INTCAPA  0x10
#define INTCAPB  0x11
#define GPIOA    0x12
#define GPIOB    0x13
#define OLATA    0x14
#define OLATB    0x15

typedef struct {
  unsigned char out_a;
  unsigned char out_b;
  unsigned char in;
} port_expander;

void configure_port_expander();
void read_port_expander(port_expander*);
